}
manifest_path = Path(sys.argv[5])

config = json.loads(base_config.read_text())
for job in config.get("jobs", []):
    key = job.pop("csv_key", None)
    if key:
        job["csv_path"] = csv_paths[key]
config.setdefault("defaults", {})["benchmark_split_manifest"] = str(manifest_path)
config["runtime_csv_paths"] = csv_paths
config["runtime_benchmark_split_manifest"] = str(manifest_path)
config["runtime_note"] = (
    "Generated by run_colab_v8b_frozen_defocus_confirmation_workflow.sh "
    "to rerun the 66-feature baseline and full defocus-difference candidates "
    "with frozen benchmark split membership."
)

if [ -z "$V6_CSV" ]; then
  echo "Missing v6 benchmark-gap cached CSV; running v6 setup workflow first."
  bash scripts/run_colab_v6_benchmark_gap100k_workflow.sh
  V6_CSV=$(ls -td training_results/feature_regression_enhanced/enhanced_v6_benchmark_gap100k_*/training_features_enhanced.csv | head -1)
fi

FROZEN_SPLIT_MANIFEST="configs/benchmark_split_v6_frozen_row_keys.json"
python3 scripts/write_benchmark_split_manifest.py \
  --csv-path "$V6_CSV" \
  --output "$FROZEN_SPLIT_MANIFEST" \
  --dataset-version enhanced_v6_benchmark_gap100k

FULL_CSV=$(ls -td training_results/feature_regression_enhanced/enhanced_v8_c1diff_full_*/training_features_enhanced.csv 2>/dev/null | head -1 || true)
if [ -z "$FULL_CSV" ]; then
  python3 scripts/augment_defocus_difference_features.py \
    --source-csv "$V6_CSV" \
    --run-prefix enhanced_v8_c1diff_full \
    --dataset-version enhanced_v8_c1diff_full \
    --mode c1_defocus_full
  FULL_CSV=$(ls -td training_results/feature_regression_enhanced/enhanced_v8_c1diff_full_*/training_features_enhanced.csv | head -1)
fi

BASE_CONFIG="configs/model_selection_batch_v8b_frozen_defocus_confirmation.json"
RUNTIME_CONFIG="colab_worker_logs/model_selection_batch_v8b_frozen_defocus_confirmation_runtime.json"
python3 - "$BASE_CONFIG" "$RUNTIME_CONFIG" "$V6_CSV" "$FULL_CSV" "$FROZEN_SPLIT_MANIFEST" <<'PY'

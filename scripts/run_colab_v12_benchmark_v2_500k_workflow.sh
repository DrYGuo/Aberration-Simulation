#!/usr/bin/env bash
set -euo pipefail

DONE_MARKER="colab_worker_logs/v12_benchmark_v2_500k_done.json"
if [ -f "$DONE_MARKER" ]; then
  echo "v12 benchmark-v2 500K retrain already completed; marker exists at $DONE_MARKER"
  exit 0
fi

mkdir -p colab_worker_logs

V11_CSV=$(ls -td training_results/feature_regression_enhanced/enhanced_v11_gap500k_*/training_features_enhanced.csv 2>/dev/null | head -1 || true)
if [ -z "$V11_CSV" ]; then
  echo "Missing cached v11 500K CSV. Do not regenerate 500K training data in this benchmark-v2 workflow."
  exit 1
fi

V12_CSV=$(ls -td training_results/feature_regression_enhanced/enhanced_v12_benchmark_v2_*/training_features_enhanced.csv 2>/dev/null | head -1 || true)
if [ -z "$V12_CSV" ]; then
  python3 scripts/generate_targeted_enhanced_dataset.py \
    --parent-csv "$V11_CSV" \
    --run-prefix enhanced_v12_benchmark_v2_500k_eval \
    --dataset-version enhanced_v12_benchmark_v2 \
    --case-counts-json configs/targeted_benchmark_v2_75k.json \
    --seed 127 \
    --batch-base-cases 256 \
    --new-row-split-hint benchmark_v2
  V12_CSV=$(ls -td training_results/feature_regression_enhanced/enhanced_v12_benchmark_v2_*/training_features_enhanced.csv | head -1)
fi

V1_SPLIT_MANIFEST="configs/benchmark_split_v6_frozen_row_keys.json"
if [ ! -f "$V1_SPLIT_MANIFEST" ]; then
  V6_CSV=$(ls -td training_results/feature_regression_enhanced/enhanced_v6_benchmark_gap100k_*/training_features_enhanced.csv 2>/dev/null | head -1 || true)
  if [ -z "$V6_CSV" ]; then
    echo "Missing v6 cached CSV required to recreate benchmark-v1 split manifest."
    exit 1
  fi
  python3 scripts/write_benchmark_split_manifest.py \
    --csv-path "$V6_CSV" \
    --output "$V1_SPLIT_MANIFEST" \
    --dataset-version enhanced_v6_benchmark_gap100k \
    --overwrite
fi

V2_SPLIT_MANIFEST="configs/benchmark_split_v12_v2_row_keys.json"
python3 scripts/write_benchmark_v2_split_manifest.py \
  --csv-path "$V12_CSV" \
  --base-manifest "$V1_SPLIT_MANIFEST" \
  --output "$V2_SPLIT_MANIFEST" \
  --new-dataset-version enhanced_v12_benchmark_v2 \
  --dataset-version enhanced_v12_benchmark_v2 \
  --overwrite

python3 scripts/report_sampling_quality.py \
  --csv-path "$V12_CSV" \
  --config configs/targeted_benchmark_v2_75k.json \
  --benchmark-split-manifest "$V2_SPLIT_MANIFEST" \
  --output-dir training_results/model_selection_reports/sampling_quality_v12_benchmark_v2_500k

BASE_CONFIG="configs/model_selection_batch_v12_benchmark_v2_500k.json"
RUNTIME_CONFIG="colab_worker_logs/model_selection_batch_v12_benchmark_v2_500k_runtime.json"
python3 - "$BASE_CONFIG" "$RUNTIME_CONFIG" "$V12_CSV" "$V2_SPLIT_MANIFEST" <<'PY'
import json
import sys
from pathlib import Path

base_config = Path(sys.argv[1])
runtime_config = Path(sys.argv[2])
csv_path = sys.argv[3]
manifest_path = sys.argv[4]

config = json.loads(base_config.read_text())
config.setdefault("defaults", {})["csv_path"] = csv_path
config.setdefault("defaults", {})["benchmark_split_manifest"] = manifest_path
config["runtime_csv_path"] = csv_path
config["runtime_benchmark_split_manifest"] = manifest_path
config["runtime_note"] = (
    "Generated by run_colab_v12_benchmark_v2_500k_workflow.sh to pin the "
    "v12 benchmark-v2 CSV and split manifest. No v12 rows are training data."
)
runtime_config.write_text(json.dumps(config, indent=2) + "\n")
print("runtime batch config:", runtime_config)
print("v12 csv:", csv_path)
print("benchmark-v2 split manifest:", manifest_path)
PY

python3 scripts/run_model_selection_batch.py \
  --batch-config "$RUNTIME_CONFIG" \
  --output-root training_results/model_selection_loop \
  --summary-root training_results/model_selection_batches \
  --max-runtime-minutes 300

RUN_DIR=$(ls -td training_results/model_selection_loop/D66_grouped_width320_lr6e-4_dropout0.075_v12benchmarkv2_500k_seed7_* 2>/dev/null | head -1 || true)
if [ -n "$RUN_DIR" ]; then
  python3 scripts/audit_s3_magnitude_metric.py --run "v12_benchmark_v2_500k_seed7=$RUN_DIR"
  python3 scripts/audit_training_validation_loss_gap.py --run "$RUN_DIR"
else
  echo "v12 benchmark-v2 seed7 run is not complete yet; worker may continue in a later cycle."
  exit 0
fi

python3 - "$DONE_MARKER" "$V12_CSV" "$V2_SPLIT_MANIFEST" "$RUN_DIR" <<'PY'
import json
import sys
from datetime import datetime, timezone
from pathlib import Path

marker = Path(sys.argv[1])
marker.write_text(
    json.dumps(
        {
            "status": "complete",
            "created_utc": datetime.now(timezone.utc).isoformat(),
            "workflow": "scripts/run_colab_v12_benchmark_v2_500k_workflow.sh",
            "dataset_csv": sys.argv[2],
            "benchmark_v2_split_manifest": sys.argv[3],
            "run_dir": sys.argv[4],
            "decision": "Evaluate v12 benchmark-v2 robustness before approving 1M training expansion.",
        },
        indent=2,
    )
    + "\n"
)
print("done marker:", marker)
PY
